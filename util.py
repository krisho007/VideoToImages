import os
import cv2


def get_fps(sVideoPath):
    video = cv2.VideoCapture(sVideoPath)

    # Find OpenCV version
    (major_ver, minor_ver, subminor_ver) = (cv2.__version__).split('.')

    if int(major_ver) < 3:
        fps = video.get(cv2.cv.CV_CAP_PROP_FPS)
    else:
        fps = video.get(cv2.CAP_PROP_FPS)

    video.release()
    return fps
